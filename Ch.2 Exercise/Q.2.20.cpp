#include<iostream>
using namespace std;
int  main(){
	int rad,area,dia,cirf;
	cout<<"\nEnter the Radius of circle:";
	cin>>rad;
	area=3.14159*rad*rad;
	cout<<"Area="<<area<<endl;
	dia=2*rad;
	cout<<"Diameter="<<dia<<endl;
	cirf=2*3.14159*rad;
	cout<<"Circumference="<<cirf<<endl;
	

}
